var currentUrl = "";
var currentFormat = "";
var pollTimer = null;

function pasteUrl() {
  navigator.clipboard.readText().then(function(text) {
    document.getElementById("urlInput").value = text.trim();
  }).catch(function() {
    showMsg("msgBox", "warn", t("pasteManually"));
  });
}

function clearUrl() {
  document.getElementById("urlInput").value = "";
  document.getElementById("formatRow").style.display = "none";
  document.getElementById("dlbtn").style.display = "none";
  document.getElementById("streambtn").style.display = "none";
  document.getElementById("fetchBtn").style.display = "";
  document.getElementById("fetchBtn").textContent = t("getQuality");
  document.getElementById("msgBox").innerHTML = "";
  currentUrl = "";
  currentFormat = "";
}

function showMsg(boxId, type, text, timeout) {
  var box = document.getElementById(boxId);
  if (!box) return;
  box.innerHTML = "<div class=\"msg " + type + "\" style=\"margin-top:14px\">" + text + "</div>";
  if (timeout !== 0) {
    setTimeout(function() { box.innerHTML = ""; }, timeout || 5000);
  }
}

function fetchFormats() {
  var url = document.getElementById("urlInput").value.trim();
  if (!url) return;
  currentUrl = url;
  var btn = document.getElementById("fetchBtn");
  btn.disabled = true;
  btn.textContent = t("gettingQuality");
  document.getElementById("msgBox").innerHTML = "";
  fetch("/formats?url=" + encodeURIComponent(url))
    .then(function(r) { return r.json(); })
    .then(function(data) {
      btn.disabled = false;
      btn.textContent = t("getQuality");
      if (data.error) {
        showMsg("msgBox", "err", t("fetchFailed") + data.error);
        return;
      }
      var sel = document.getElementById("formatSelect");
      sel.innerHTML = "";
      // 翻译前三个固定选项 / Translate fixed options / 固定選択肢を翻訳
      if (data.formats[0]) data.formats[0].label = t("bestQuality");
      if (data.formats[1]) data.formats[1].label = t("bestSingle");
      if (data.formats[2]) data.formats[2].label = t("bestAudio");
      for (var i = 3; i < data.formats.length; i++) {
        data.formats[i].label = data.formats[i].label.replace("Audio only", t("audioOnly"));
      }
      data.formats.forEach(function(f) {
        var opt = document.createElement("option");
        opt.value = f.id;
        opt.textContent = f.label;
        sel.appendChild(opt);
      });
      document.getElementById("formatRow").style.display = "block";
      document.getElementById("dlbtn").style.display = "";
      document.getElementById("dlbtn").textContent = t("saveToServer");
      document.getElementById("streambtn").style.display = "";
      document.getElementById("streambtn").textContent = t("directDownload");
    })
    .catch(function() {
      btn.disabled = false;
      btn.textContent = t("getQuality");
      showMsg("msgBox", "err", t("fetchFailedRetry"));
    });
}

function startStream() {
  currentFormat = document.getElementById("formatSelect").value;
  var canStream = (currentFormat === "best" || currentFormat === "bestaudio/best");
  if (!canStream) {
    document.getElementById("streamWarnModal").classList.add("show");
    return;
  }
  showMsg("msgBox", "ok", t("pushing"), 8000);
  var streambtn = document.getElementById("streambtn");
  streambtn.disabled = true;
  fetch("/streamtoken", {
    method: "POST",
    headers: {"Content-Type": "application/json"},
    body: JSON.stringify({url: currentUrl, format: currentFormat})
  }).then(function(r) { return r.json(); }).then(function(data) {
    streambtn.disabled = false;
    if (data.token) {
      var link = window.location.origin + "/stream/" + data.token;
      document.getElementById("streamLinkText").textContent = link;
      document.getElementById("streamLinkModal").classList.add("show");
    } else {
      showMsg("msgBox", "err", t("fetchFailedRetry"));
    }
  }).catch(function() {
    streambtn.disabled = false;
    showMsg("msgBox", "err", t("fetchFailedRetry"));
  });
}

function doStreamDownload() {
  window.location.href = document.getElementById("streamLinkText").textContent;
}

function doStreamCopy() {
  var text = document.getElementById("streamLinkText").textContent;
  navigator.clipboard.writeText(text).then(function() {
    showMsg("msgBox", "ok", t("linkCopied"), 3000);
    closeModal("streamLinkModal");
  }).catch(function() {
    showMsg("msgBox", "warn", t("copyManually"), 0);
  });
}

function startDownload(overwrite) {
  currentFormat = document.getElementById("formatSelect").value || "bestvideo+bestaudio/best";
  var btn = document.getElementById("dlbtn");
  var taskArea = document.getElementById("taskArea");
  btn.disabled = true;
  btn.textContent = t("submitting");
  taskArea.innerHTML = "<div class=\"progress-wrap\" style=\"margin-top:14px\"><div class=\"progress-bar-bg\"><div class=\"progress-bar\" style=\"width:5%\"></div></div><div class=\"progress-text\">" + t("connecting") + "</div></div>";
  fetch("/start", {
    method: "POST",
    headers: {"Content-Type": "application/json"},
    body: JSON.stringify({url: currentUrl, format: currentFormat, overwrite: overwrite || false})
  }).then(function(r) { return r.json(); }).then(function(data) {
    btn.disabled = false;
    btn.textContent = t("saveToServer");
    if (data.exists) {
      taskArea.innerHTML = "";
      document.getElementById("existsFilename").textContent = data.filename + t("existsConfirm");
      document.getElementById("existsModal").classList.add("show");
      return;
    }
    if (data.error) {
      taskArea.innerHTML = "";
      showMsg("msgBox", "err", data.error);
      return;
    }
    startPolling();
  }).catch(function() {
    btn.disabled = false;
    btn.textContent = t("saveToServer");
    taskArea.innerHTML = "";
    showMsg("msgBox", "err", t("submitFailed"));
  });
}

function confirmOverwrite() {
  closeModal("existsModal");
  startDownload(true);
}

function closeModal(id) {
  document.getElementById(id).classList.remove("show");
}

function startPolling() {
  if (pollTimer) clearInterval(pollTimer);
  pollTimer = setInterval(pollStatus, 2000);
  pollStatus();
}

function pollStatus() {
  fetch("/status").then(function(r) { return r.json(); }).then(function(data) {
    var taskArea = document.getElementById("taskArea");
    if (data.running) {
      taskArea.innerHTML = "<div class=\"progress-wrap\" style=\"margin-top:14px\"><div class=\"progress-bar-bg\"><div class=\"progress-bar\" style=\"width:" + data.percent + "%\"></div></div><div class=\"progress-text\">" + t("downloading") + data.percent.toFixed(1) + "%</div></div>";
    } else if (data.success === true) {
      clearInterval(pollTimer);
      taskArea.innerHTML = "<div class=\"msg ok\" style=\"margin-top:14px\">" + t("downloadComplete") + "</div>";
      setTimeout(function() { taskArea.innerHTML = ""; }, 3000);
      refreshFiles();
    } else if (data.success === false) {
      clearInterval(pollTimer);
      var errMsg = data.error === "cookie_expired" ? t("cookieExpired") : data.error;
      taskArea.innerHTML = "<div class=\"msg err\" style=\"margin-top:14px\">" + errMsg + "</div>";
      setTimeout(function() { taskArea.innerHTML = ""; }, 5000);
    } else {
      clearInterval(pollTimer);
      taskArea.innerHTML = "";
    }
  });
}

function refreshFiles() {
  fetch("/filelist").then(function(r) { return r.json(); }).then(function(files) {
    var container = document.getElementById("fileList");
    if (!container) return;
    document.getElementById("fileCount").textContent = files.length;
    if (!files.length) {
      container.innerHTML = "<div class=\"empty\">" + t("noFiles") + "</div>";
      return;
    }
    var html = "";
    files.forEach(function(f) {
      var safe = f.name.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;").replace(/"/g, "&quot;");
      var enc = encodeURIComponent(f.name);
      var jsname = f.name.replace(/\\/g, "\\\\").replace(/'/g, "\\'");
      html += "<div class=\"file-card\">" +
        "<div class=\"file-name\">" + safe + "</div>" +
        "<div class=\"file-meta\">" + f.size + " &middot; " + f.time + "</div>" +
        "<div class=\"file-actions\">" +
        "<a href=\"/file/" + enc + "\"><button class=\"btn-blue btn-sm\">" + t("download") + "</button></a>" +
        "<button class=\"btn-purple btn-sm\" onclick=\"copyLink('" + jsname + "')\">" + t("copyLink") + "</button>" +
        "<button class=\"btn-gray btn-sm\" onclick=\"deleteFile('" + jsname + "')\">" + t("deleteFile") + "</button>" +
        "</div></div>";
    });
    container.innerHTML = html;
  });
}

function copyLink(filename) {
  fetch("/token", {
    method: "POST",
    headers: {"Content-Type": "application/json"},
    body: JSON.stringify({filename: filename})
  }).then(function(r) { return r.json(); }).then(function(data) {
    if (data.token) {
      document.getElementById("linkText").textContent = window.location.origin + "/dl/" + data.token;
      document.getElementById("linkModal").classList.add("show");
    }
  });
}

function doCopyLink() {
  var text = document.getElementById("linkText").textContent;
  navigator.clipboard.writeText(text).then(function() {
    closeModal("linkModal");
    showMsg("msgBox", "ok", t("linkCopied"), 3000);
  }).catch(function() {
    showMsg("msgBox", "warn", t("copyManually"), 0);
  });
}

function deleteFile(name) {
  if (!confirm(t("confirmDelete"))) return;
  fetch("/delete", {
    method: "POST",
    headers: {"Content-Type": "application/x-www-form-urlencoded"},
    body: "filename=" + encodeURIComponent(name)
  }).then(function() { refreshFiles(); });
}

function addLangSwitcher() {
  var div = document.createElement("div");
  div.style.cssText = "position:fixed;top:12px;right:12px;z-index:999;display:flex;gap:4px;";
  div.innerHTML =
    "<button id=\"langBtn_ja\" onclick=\"switchLang('ja')\" style=\"padding:4px 8px;font-size:0.78em;border-radius:4px;background:#444;color:#eee;border:none;cursor:pointer;\">日</button>" +
    "<button id=\"langBtn_en\" onclick=\"switchLang('en')\" style=\"padding:4px 8px;font-size:0.78em;border-radius:4px;background:#444;color:#eee;border:none;cursor:pointer;\">EN</button>" +
    "<button id=\"langBtn_zh\" onclick=\"switchLang('zh')\" style=\"padding:4px 8px;font-size:0.78em;border-radius:4px;background:#444;color:#eee;border:none;cursor:pointer;\">中</button>";
  document.body.appendChild(div);
  updateLangButtons();
}

window.onload = function() {
  currentLang = detectLang();
  addLangSwitcher();
  applyLang();
  fetch("/status").then(function(r) { return r.json(); }).then(function(data) {
    if (data.running) startPolling();
    else if (data.success === true) {
      showMsg("taskArea", "ok", t("lastDownloadDone"), 3000);
    }
  });
};
