var currentLang = "en";

function detectLang() {
  var saved = localStorage.getItem("ytdl_lang");
  if (saved && i18n[saved]) return saved;
  var nav = (navigator.language || "en").toLowerCase();
  if (nav.indexOf("zh") === 0) return "zh";
  if (nav.indexOf("ja") === 0) return "ja";
  return "en";
}

function t(key) {
  return (i18n[currentLang] && i18n[currentLang][key]) || (i18n["en"][key]) || key;
}

function switchLang(lang) {
  currentLang = lang;
  localStorage.setItem("ytdl_lang", lang);
  applyLang();
  updateLangButtons();
  // 实时更新画质列表前三项 / Update format labels in real-time / 画質リストを即時更新
  var sel = document.getElementById("formatSelect");
  if (sel && sel.options.length >= 3) {
    sel.options[0].textContent = t("bestQuality");
    sel.options[1].textContent = t("bestSingle");
    sel.options[2].textContent = t("bestAudio");
  }
}

function updateLangButtons() {
  ["ja", "en", "zh"].forEach(function(l) {
    var btn = document.getElementById("langBtn_" + l);
    if (btn) {
      btn.style.opacity = (l === currentLang) ? "1" : "0.4";
      btn.style.fontWeight = (l === currentLang) ? "bold" : "normal";
    }
  });
}

